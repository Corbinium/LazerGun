# Function to generate a single signal based on the "at least 6 lines" rule
def combine_signals_at_least_6(signals, threshold):
    # Transpose the signals to work column by column
    columns = list(zip(*signals))
    # Apply the rules: 0 if at least 6 are 0, 1 if at least 6 are 1, ' ' otherwise
    combined = ''.join(['0' if sum(c == '0' for c in col) >= threshold else
                        '1' if sum(c == '1' for c in col) >= threshold else
                        ' ' for col in columns])
    return combined

